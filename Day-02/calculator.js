var calculator = {
	add : function(operands){
		return operands.n1 + operands.n2;
	},
	subtract : function(operands){
		return operands.n1 - operands.n2;
	},
	multiply : function(operands){
		return operands.n1 * operands.n2;
	},
	divide : function(operands){
		return operands.n1 / operands.n2;
	}
};